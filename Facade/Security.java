package facadeDesign;

public class Security {
	
	public void alarmOn()
	{
		System.out.println("DANGER DEDUCTED!! Turning ON security alarm!!");
	}
	
	public void alarmOff()
	{
		System.out.println("Turning OFF security alarm.");
	}
	
	public void disableAlarm()
	{
		System.out.println("Security alarm turned OFF.");
	}

}
